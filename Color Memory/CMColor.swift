    static func cmColorLightGreen() -> UIColor{
        return UIColor(red: CGFloat(0x16)/255, green: CGFloat(0x94)/255, blue: CGFloat(0x87)/255, alpha: 1.0)
    }
    
    static func cmColorYellow() -> UIColor{
        return UIColor(red: CGFloat(0xfd)/255, green: CGFloat(0xe8)/255, blue: CGFloat(0x4d)/255, alpha: 1.0)
    }
    
    
    static func cmColorOrange() -> UIColor{
        return UIColor(red: CGFloat(0xfc)/255, green: CGFloat(0x95)/255, blue: CGFloat(0x27)/255, alpha: 1.0)
    }
    
    static func cmColorRed() -> UIColor{
        return UIColor(red: CGFloat(0xe5)/255, green: CGFloat(0x23)/255, blue: CGFloat(0x65)/255, alpha: 1.0)
    }
    
    
    static func cmColorViolet() -> UIColor{
        return UIColor(red: CGFloat(0x9b)/255, green: CGFloat(0x59)/255, blue: CGFloat(0xb6)/255, alpha: 1.0)
    }
    
    //673ab7
    static func cmColorDarkViolet() -> UIColor{
        return UIColor(red: CGFloat(0x67)/255, green: CGFloat(0x3a)/255, blue: CGFloat(0xb7)/255, alpha: 1.0)
    }
    
    static func cmColorBlack() -> UIColor{
        return UIColor(red: CGFloat(0x34)/255, green: CGFloat(0x49)/255, blue: CGFloat(0x5e)/255, alpha: 1.0)
    }
    
}
